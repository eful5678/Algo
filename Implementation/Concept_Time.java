import java.util.Scanner;

public class Concept_Time {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.println("첫번째 정수 a를 입력해주세요 : ");
        int a = sc.nextInt();

        System.out.println("두번째 정수 b를 입력해주세요 : ");
        int b = sc.nextInt();

        System.out.println("입력 값 a : " + a);
        System.out.println("입력 값 b : " + b);
    }
}
